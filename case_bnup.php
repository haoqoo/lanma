<p>概述</p>
<blockquote>北京师范大学出版社多年来积累了大量图片资料，包括photoshop、coreldraw等软件的图片格式。多年来图片资源一直分散存放，没有统一的管理，图片资源不能共享，查找极其不便。现在利用原有的网络资源和超级图库管理软件网络版来管理，实现了“软件+网络”的管理方式，使得图库资源能够在各分社和各部门之间共享，提高了工作效率和各部门之间的协作。同时使图库资源的安全性得到了保障。</blockquote>
<p>具体应用</p>
<blockquote>1．通过批量上传和快速分类录入、整理几万张图片资料，包括photoshop、coreldraw等软件的图片格式。<br>
2．大约有200多个用户，由管理员统一管理，根据各部门和人员的不同职责分成不同的用户组。<br>
3．分别给用户/用户组分配权限。<br>
4．根据给文件、文件夹添加的详细信息、预先设定的关键词达到快速检索。<br>
5．在图库里直接调用photoshop、coreldraw软件对psd、cdr的文件格式进行编辑。<br>
6．对正在编辑的文件加上文件锁，防止多个用户同时打开同一文件进行编辑。<br>
7．通过访问日志功能记录所有用户访问图库的操作行为。<br>
</blockquote>
