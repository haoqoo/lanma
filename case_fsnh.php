        <h2 align="center">SuperLib软件在规划管理行业中的应用</h2>
        

     
<div class="content">
    <h3>概况说明</h3>
    <p>规划设计院主要从事CAD图纸设计，因为图片很多，使得存放、共享、管理、查找都比较困难。图片的相关信息如何才能一幕了然呢？如果没有对图片进行详细的分类和说明根本就没有办法做到，这也是编辑们最头疼的问题。面对大量的图片如果一一进行分类和添加说明是一项非常繁重的工作，由于Windows中文件的说明和搜索功能有限，即使文件有明细的分类和说明也很难准确的搜索到指定的文件。</p>
    <h3>需求分析</h3>
    <ul style="margin-right:20px;">
      <li>要能够实现网络共享；</li>
      <li>要求增加图片简单，各级目录清晰，分类方便，查找快捷；</li>
      <li>软件需要有强大的搜索功能能够依据图片的日期和内容进行搜索；</li>
      <li>容易分辨图片使用情况；</li>
      <li>软件提供备份功能,能够对历史文件进行备份；</li>
      <li>对重要文件可以限制访问；</li>
      <li>能对图片进行批量的分类操作和说明操作；</li>
      <li>能够使用软件对图片进行浏览或生成缩略图查看；</li>
      <li>对常用的图片编辑软件有很好的支持；</li>
      <li>因为图库很大，要求能增加新的硬盘；</li>
      <li>要有权限管理功能，能对每个用户进行不同权限的分配。</li>
    </ul>
    <h3>解决方案</h3>
    <p>基于以上需求，北京蓝码动力科技有限公司开发了SuperLib软件。SuperLib软件针对于设计师的工作需求具有非常丰富和实用的功能。如下是SuperLib软件对于建筑装潢设计公司需求的解决方案：</p>
    <div class="image"><img src="images/fuwuqi.gif" alt=""></div>
    <ol style="margin-right:20px;">
      <li>SuperLib软件网络版使用 C/S 模式，在一台计算机中安装服务器端用来存储图库资源，其它计算机中安装客户端作为图库使用平台；这样使用网络将图库资源共享并同时提供给多个用户使用，有效提高图库资源的利用率。</li>
      <li>用户使用SuperLib软件可以为文件添加关键词(说明信息)，关键词的内容有用户自己定义，可以为图纸的作者、使用日期、图纸内容等，用户在搜索时可以更具这些关键词对图片进行查找。用户可以对关键词进行模糊查询选择关键词之间的逻辑关系，查找精确快捷。</li>
      <li>用户可以使用软件中的导出图库资源包对图库资源进行备份操作，若需要使用历史备份资源时可以使用导入图库资源包讲备份的文件重新导入到图库中。</li>
      <li>级图库管理软件有很好的安全机制，管理员可根据每个人的使用状况，对他们进行权限设置。对于重要文件夹可以禁止他人浏览。</li>
      <li>SuperLib软件可以将文件进行细致的分类并添加详细说明，并支持同时对多个文件分类和添加关键词。</li>
      <li>SuperLib软件支持常见图片的浏览，并可对30余种图形/图像文件生成缩略图。</li>
      <li>对于图库中的图片，可以SuperLib软件中的外部命令选择其它图片编辑软件进行打开操作。</li>
      <li>SuperLib软件服务器端有存储区管理功能，能无限增加新的存储区。</li>
    </ol>
    <h3>使用情况</h3>
    <p>此规划设计院使用图库人数50人左右，一共购买了20个节点；用户按照设计室进行分类。共设定了3个管理员，根据设计室的不同设置人员权限，本室设计人员可修改、上传，外室设计人员可浏览、下载，无权修改。 图库硬盘容量为200G，现使用图库容量100G左右；主要为DWG文件，另有JPG、PLT文件。图库目录按图纸类别分，暂时没有考虑预设关键词。</p>
