    <p>天津某高校有一个摄影协会，他们有许多会员，平时拍有很多需要保存的照片，然而随着照片数量的增多，管理起来非常的不方便，尤其是在大量的照片中寻找需要的那一张时，要花费大量的时间。在此期间，他们使用了如Picasa这样的软件，很方便实用，然后又有一个问题出现了，这些都只有单机版，而他们需要有多个人能同时远程访问到这些照片库，所以这家摄影协会急需要一个网络版的照片管理软件。</p>
    <h3>摄影协会的需求</h3>
    <ol>
      <li>这个软件必须能对多种图片格式有很好的支持作用，要能够生成缩略图；</li>
      <li>这个软件必须对其它软件有很好的支持，最好把软件中的照片直接用Photoshop直接打开；</li>
      <li>这个软件必须是网络版的，能使用户通过网络对图库进行远程的操作使用；</li>
      <li>这个软件必须能对保存的照片增加一些属性说明，而且查找时能对这些属性说明进行查找；</li>
      <li>这个软件必须支持多层目录结构，而且用户能对每个目录任意增加名称；</li>
      <li>这个软件必须有方便的查找功能，能对照片的多个属性和名称进行查找；</li>
      <li>这个软件必须支持对多个照片进行同时操作；</li>
      <li>这个软件必须有权限管理功能，能给不同级别的用户提供不同的权限；</li>
      <li>这个软件必须有日记记录功能，能记录下哪个用户什么时间对照片做了什么操作；</li>
