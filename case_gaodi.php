    <h3>建筑装饰设计公司图库使用概况</h3>
    <p>建筑装潢设计公司主要以建筑室内装潢设计为主，公司有几万张图形/图像文件。公司的图库文件一般刻录在光盘中或分散在设计师的计算机中进行存储，当设计师需要一张图片素材时往往花很多时间在上千张的光盘中查找，对于图库资料的管理和存储以及设计师的应用公司耗费了大量的人力和财力而且管理和使用的效率很低。</p>
    <h3>建筑装潢设计公司图库管理软件需求</h3>
    <ul style="margin-right:20px;">
      <li>由于光盘损坏或流失容易造成图库资源丢失，所以对图库资源的存储最好能够存放在计算机中。</li>
      <li>图库资源能够同时提供给多个设计师使用，以提高图库资源的使用效率。</li>
      <li>由于图库资源中文件数目庞大、类别复杂，所以最好能够对图库中的文件进行详细的分类存储，而且对于一些多用途文件可以进行多重的分类。</li>
      <li>能够支持多种文件类型，能够对图库资源进行浏览对于常见文件能够生成缩略图预览。</li>
      <li>能对图库文件进行详细的说明，查看说明能够让设计师初步了解文件的内容。</li>
      <li>软件的搜索功能至关重要，使设计师能够在最短的时间内搜索到指定的文件。</li>
      <li>对其它图形/图像编辑软件有很好的支持，在查找到文件后能够打开或者预览文件。</li>
      <li>图库资源中存在重复文件使图库数据庞大，不利于管理，所以软件需要能够识别图库资源中的重复文件，使图库资源尽量精简。</li>
      <li>要防治图库资源人为流失或重要设计资料被窃取，需要有一定的安全机制来控制每个用户对图库的操作。</li>
    </ul>
    <h3>解决方案</h3>
    <p>基于以上需求，北京蓝码动力科技有限公司开发了SuperLib软件。SuperLib软件针对于设计师的工作需求具有非常丰富和实用的功能。如下是SuperLib软件对于建筑装潢设计公司需求的解决方案：</p>
