      <li>SuperLib软件网络版使用 C/S 模式，在一台计算机中安装服务器端用来存储图库资源，其它计算机中安装客户端作为图库使用平台；这样使用网络将图库资源共享并同时提供给多个用户使用，有效提高图库资源的利用率。</li>
