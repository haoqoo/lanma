  <p><a name="item1_3"></a>问: 购买后，如何得到单机版的注册码?</p>
  <blockquote>首先安装好超级图库管理软件单机版，然后运行它，在服务器软件界面上按“注册”按钮，进入注册对话框。自己取一个用户名，将用户名和申请码发送给我们，我们会计算出注册码发送给您。您再将注册码、用户名准确填入注册对话框，按“注册”按钮即可注册成功。</blockquote>
  <p><a name="item1_4"></a>问: 我现在用的是单机版，将来要升级成网络版，数据能保留吗？</p>
  <blockquote>可以的。超级图库网络版和单机版的存储区结构和数据库的数据结构是相同的，因此在安装了网络版之后，用单机版的数据库和文件存储区覆盖网络版的数据库和文件存储区即可。购买网络版后，可联系我们进行在线协助。</blockquote>
