        <h2 align="center">SuperLib软件在北京市城市规划设计研究院中的应用</h2>
        

     
<div class="content">
<p>概述</p>
<blockquote>北京市城市规划设计研究院将《超级图库软件》（SuperLib）用于照片的管理。这些照片中，既有胶片照片经扫描形成的电子文件，也有用数码相机拍摄的照片。照片的内容都是关于城市的建筑和街景，许多都是珍贵的老照片。在每次拆迁之前，都会有专人去拍摄拆迁地点，这些影像在拆迁之后就再也不复存在了。这些照片所占的磁盘空间已达数百G，查找起来非常困难。规划院的领导希望把这些照片利用起来。在应用了超级图库软件之后，照片资料得到了共享，并且给不同的部门和级别分配了不同的权限。根据规划院的具体情况，还定制了借阅和自动水印等功能。</blockquote>
<p>解决方案</p>
<blockquote>本公司针对规划院的特殊需求，对现有的SuperLib图库管理软件进行了定制开发，增加以下功能：<br>
1．增加借阅单处理功能。员工可以浏览服务器中的图片，并将选定的内容加入借阅单，并通过图库系统提交借阅单。图库管理员负责处理借阅单，决定是否同意借阅，并通过图库系统将同意借阅的内容打包，然后图库管理员用电子邮件等方式将文件包传送给借阅者。<br>
2．目前图库系统自动生成的缩略图尺寸太小，将增加较大尺寸的预览图，如640x480, 800x600等尺寸，方便员工浏览。<br>
3．下载的图片上可选择添加水印或不添加水印。通过权限控制是否能够下载无水印的照片。<br>
4．结合现有的办公自动化系统的用户数据库，对用户和角色进行权限控制。<br>
以上增加的功能均同时在C/S架构和B/S架构实现，员工可自由选择使用图库客户端软件操作，或用网页浏览器操作。</blockquote>
<p>应用情况</p>
<blockquote>此项目已开发、实施完毕，正式投入使用近一年，效果良好。</blockquote>
