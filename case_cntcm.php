<p>需求</p>
<blockquote>中医药报有很多照片，需要录入、整理、存储、和共享，同时对权限有严格要求。主要需求如下：<br>
1. 照片需要分类整理，以多级文件夹的形式存储。<br>
2. 每张照片都要能录入对该照片内容的说明。<br>
3. 能够记录上传照片的录入人及录入时间，以及拍摄该照片的拍摄人及拍摄时间。管理员还能自行定义新的字段，或者编辑/删除已有的字段。<br>
4. 能够进行搜索，快速查找到所需的照片。既要求能模糊搜索，又要求能指定具体条件进行精确搜索。<br>
5. 要求录入方便。原来他们用的是新华社的一套系统，录入非常麻烦。<br>
6. 要求有权限管理系统，什么人对什么文件夹有什么权限，管理员都能进行设置。<br>
</blockquote>
<p>应用</p>
<blockquote>报社购买了无节点限制的C-S架构（即客户端-服务器架构）的版本，专门为超级图库管理软件购买了一台IBM服务器，内装500G的硬盘。管理员在自己的电脑上安装了超级图库控制台软件，对服务器进行远程管理。在录入人员的电脑上安装了超级图库客户端软件，可进行录入和浏览、查询等操作。管理员具有所有权限，录入人员只对部分文件夹具有部分权限。<br>
管理员预先建立好若干文件夹，录入人员将图片文件录入到这些文件夹中，并给每个文件和文件夹填写注释说明。使用自定义属性功能，定义了照片的编号、作者、创建日期、拍摄地点、主要人物、主题等附加信息字段。按照会议、医药图片、专家照片等自定义关键词。可以通过这些说明、属性、关键词等进行模糊搜索或者精确搜索，检索出符合内容的图片。通过访问日志，可以查到用户在某一时段对图库的操作。
</blockquote>
<p>应用情况</p>
<blockquote>从2007年购买软件至今，应用效果良好。</blockquote>
